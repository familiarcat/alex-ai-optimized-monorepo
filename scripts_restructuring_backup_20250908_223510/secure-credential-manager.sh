# Function to load credentials from ~/.zshrc
load_credentials() {
    echo "â„¹ï¸  Loading credentials from ~/.zshrc..."
    
    # Source ~/.zshrc to get environment variables
    if [ -f ~/.zshrc ]; then
        # Extract only environment variable exports from ~/.zshrc
        # This avoids issues with zsh-specific commands
        while IFS= read -r line; do
            if [[ $line == export* ]]; then
                eval "$line" 2>/dev/null || true
            fi
        done < ~/.zshrc
        echo "âœ… Credentials loaded from ~/.zshrc"
    else
        echo "âŒ ~/.zshrc not found"
        exit 1
    fi
}

# Function to validate required credentials
validate_credentials() {
    echo "â„¹ï¸  Validating required credentials..."
    
    required_vars=(
        "SUPABASE_URL"
        "SUPABASE_ANON_KEY"
        "N8N_URL"
        "N8N_API_KEY"
    )
    
    missing_vars=()
    for var in "${required_vars[@]}"; do
        if [ -z "${!var}" ]; then
            missing_vars+=("$var")
        fi
    done
    
    if [ ${#missing_vars[@]} -eq 0 ]; then
        echo "âœ… All required credentials are present"
        return 0
    else
        echo "âŒ Missing required credentials:"
        for var in "${missing_vars[@]}"; do
            echo "  - $var"
        done
        return 1
    fi
}

